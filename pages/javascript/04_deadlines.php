  <p>Coursera offers suggested deadlines. There is no penalty for missing a deadline, but in session-based courses, you may need to switch to the next session if you fall too far behind. After you enroll in the course, youâ€™ll be able to see the suggested deadlines for all assignments by visiting the <b>Assignments</b> tab on the left sidebar. You can also go to a specific week to see deadlines for that week's assignments.</p>
  <p>If you are in a course session with a specific start and end date, you'll need to pass all assignments before the end date to complete the course in your current session. If you aren't able to finish before the end date, you can switch to the next session. When you switch sessions, your work will transfer with you, so you don't need to resubmit assignments that you passed in the previous session.</p>
